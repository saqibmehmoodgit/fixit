package com.fixit.domain.bo;

public class QueryReviewBo {

	 private String memberName;
	 private String fixername;
	 private String queryTitle;
	 private String queryHashCode;
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getFixername() {
		return fixername;
	}
	public void setFixername(String fixername) {
		this.fixername = fixername;
	}
	public String getQueryTitle() {
		return queryTitle;
	}
	public void setQueryTitle(String queryTitle) {
		this.queryTitle = queryTitle;
	}
	public String getQueryHashCode() {
		return queryHashCode;
	}
	public void setQueryHashCode(String queryHashCode) {
		this.queryHashCode = queryHashCode;
	}
	 
	 
}

